// Retorno da função, toda função precisa retornar algo
function nomeCompleto(nome, sobrenome) {
  return `${ nome } ${ sobrenome }`;
}

// Armazenando o retorno da função na variável completo
let completo = nomeCompleto("Erivelton", "Rodrigues");
console.log(`Nome completo: ${ completo }`);

