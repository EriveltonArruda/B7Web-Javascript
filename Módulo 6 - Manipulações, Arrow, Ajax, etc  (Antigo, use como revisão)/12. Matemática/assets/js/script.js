// Arredonda dependendo do número passado
let novoValor = Math.round(3.50);
console.log(novoValor);

// Arredonda para baixo
let baixo = Math.floor(3.50);
console.log(baixo);

// Arredonda para cima
let cima = Math.ceil(3.50);
console.log(cima);

// Retorna o valor absoluto
let absoluto = Math.abs(-9.65464);
console.log(absoluto);

// Retorna o menor número dentre os passados no parâmetro
let min = Math.min(7, 100, 600, 20, 3);
console.log(min)

// Retorna o maior número dentre os passados no parâmetro
let maior = Math.max(7, 100, 600, 20, 3);
console.log(maior);

// Gera números aleatórios entre 0 e 1
let aleatorio = Math.random();
console.log(aleatorio);

// Gera números aleatórios de 0 a 100
let random = Math.round(Math.random() * 100);
console.log(random);